// # What is String Index?
// - In JavaScript when we use String it has multiple characters in it.
// - When we total sum the character in String we get length.
// - That mean one single character is on specific number.
// - That specific number will known as index.

// Example:
var name = "Brijesh";
// B r i j e s h
// 0 1 2 3 4 5 6

// # Why use String Index?
// - To access specific element in the String.

// # How to use String Index?

// ## Syntax:
// variableName[Index Number]

let name = "Hemil";
console.log(name[3]);