// Arithmetic Operators

let variable = 5;
let variable1 = 2;
console.log(variable + variable1); // Addition
console.log(variable - variable1); // Subtraction
console.log(variable * variable1); // Multiplication
console.log(variable ** variable1); 
console.log(variable / variable1);
console.log(variable % variable1);
console.log(variable++);
console.log(variable--);
