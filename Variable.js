// Topic: Variable

// # What is Variable?
// - Variable is like an box.
// - We store item in the box.
// - Same variable store a value inside.
// - Variable (Box) = Value (Item)

// # Why use Variable?
// - We can store value.
// - We can use the value later.
// - We can change the stored value later.

// # How to create a Variable?
var name = "Brijesh";

// # How to use a Variable?
console.log(name);

// # How to change the value of Variable?
name = "Hemil";
console.log(name);
