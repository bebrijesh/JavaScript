// IF - Else

// # What is If-Else?
// - It's conditional statement.

// # Why use If-Else?
// - It's use to check if the condition true code will be executed.
// - Else condition is not satisfied then else part will be executed.

// # Syntax:
/* 
    if (condition){
        Code...
    } else {
        Code...
    }
*/

let age = 20;
if (age >= 18) {
  console.log("You can give a vote.");
} else {
  console.log("You can't give a vote.");
}
