// var Keyword

// Note
// - `var` keyword has been deprecated so it's not recommended to use.
// - It can be initialized later.
// - It can be redeclare.
// - It can be reassigned.
// - It has global scope.

// Syntax
// var variableName = value;

// Initialized later
var variable;
variable = "value";

// Redeclare
var variable = "value";
var variable = "value";

// Reassigned
var variable = "value1";
variable = "value2";