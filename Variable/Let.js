// Let Keyword

// NOTE
// - It can be initialized later.
// - It can be reassigned.
// - It can't be redeclare.

// SYNTAX
// let variableName = value;

// Initialized later
let variable;
variable = "value";

// Reassigned
let variable1 = "value1";
variable1 = "value2";

// Can't be redeclare
// let variable2 = "value1";
// let variable2 = "value2";
