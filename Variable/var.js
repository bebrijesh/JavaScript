// # What is var? 
// - It's use for creating a variable.

// # Why use var? 
// - When we create variable we need to define it's datatype according to it's value.
// - For example: 
// - If the variable contain number like 123 meaning it has integer datatype.
// - We have to mention that when we create variable int number = 123;
// - When you use var keyword and create variable it will automatically takes the datatype according to it's assign value.
// - So, user doesn't have to type datatype every single time when create the variable.

// # How to use var?
var name = "Brijesh"
var number = 123