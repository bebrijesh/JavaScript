// For

/* 
    # What is For?
    - It's loops through a block of code a number of times
    
    # Syntax: 
    - The for statement creates a loop with 3 optional expressions:
    - Expression 1 (Variable): is executed (one time) before the execution of the code block.
    - Expression 2 (Condition/ Range): defines the condition for executing the code block.
    - Expression 3 (Increment/ Decrement): is executed (every time) after the code block has been executed.
    
    for (variable; condition/ range; increment/ decrement) {
        Code...
    }
*/

for (let i = 0; i <= 10; i++) {
  console.log(i);
}
