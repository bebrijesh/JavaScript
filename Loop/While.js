// While Loop

/* 
    # What is While Loop?
    - The while loop loops through a block of code as long as a specified condition is true.

    # Syntax:
    while(condition){
        Code...
    }

    !NOTE:
    - If you forget to increase the variable used in the condition, the loop will never end. This will crash your browser.
*/

let i = 0
while (i <= 10) {
  console.log(i);
  i++;
}
