// Arithmetic Operators

// # What is Arithmetic Operators?
// - It's use to perform arithmetic operation on numbers.
// - Arithmetic operation also known as math operation.

// # List of Arithmetic Operators:
// + Addition
// - Subtraction
// * Multiplication
// ** Exponentiation
// / Division
// % Remainder
// ++ Increment
// -- Decrement

let a = 5;
let b = 2;
console.log(a + b);
console.log(a - b);
console.log(a * b);
console.log(a ** b);
console.log(a / b);
console.log(a % b);
console.log(a++);
console.log(a--);
