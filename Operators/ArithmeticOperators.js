// Arithmetic Operators

// # What is Arithmetic Operators?
// - It's use to perform arithmetic operation on numbers.
// - Arithmetic operation also known as math operation.

// # How to use Arithmetic Operators?
// ## List of Arithmetic Operators:
// 1. + = Addition
// 2. - = Subtraction
// 3. * - Multiplication
// 4. / = Division
// 5. % = Remainder
// 6. ** = Exponentiation
// 7. ++ = Increment
// 8. -- = Decrement

// 1. + = Addition
var a = 1;
var b = 2;
var c = a + b;
console.log(c);

// 2. - = Subtraction
var a = 1;
var b = 2;
var c = a - b;
console.log(c);

// 3. * = Multiplication
var a = 1;
var b = 2;
var c = a * b;
console.log(c);

// 4. / = Division
var a = 1;
var b = 2;
var c = a / b;
console.log(c);

// 5. % = Remainder
var a = 1;
var b = 2;
var c = a % b;
console.log(c);

// 6. ** = Exponentiation 
var a = 1;
var b = 2;
var c = a ** b; // 1^2
console.log(c);

// 7. ++ = Increment
var a = 1;
a++;
console.log(a);

// 8. -- = Decrement
var a = 1;
a--;
console.log(a);
