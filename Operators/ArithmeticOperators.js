// Arithmetic Operators

let number1 = 5;
let number2 = 2;
console.log(number1 + number2);
console.log(number1 - number2);
console.log(number1 * number2);
console.log(number1 ** number2);
console.log(number1 / number2);
console.log(number1 % number2);
console.log(number1++);
console.log(number1--);
