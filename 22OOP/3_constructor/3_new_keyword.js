// `new` Keyword

// Q. What is `new` Keyword?
// - In JavaScript, the new keyword is used to create an instance of an object or to invoke a constructor function. When used with a constructor function, it performs the following steps:
// - Creates a new empty object.
// - Binds the newly created object as the `this` keyword within the constructor function.
// - Sets the prototype of the newly created object to the prototype property of the constructor function.
// - Executes the constructor function, which initializes the newly created object with properties and methods.
// - Returns the newly created object.