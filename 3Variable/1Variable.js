// Variable

// NOTE
// - To create variable you can use var, let and const keyword.
// - `var` keyword has been deprecated so it's not recommended to use.

// Single variable
let var1 = 1;
let var2 = 2;

// Multiple variable
let var3, var4, var5;
var3 = var4 = var5 = 1;

// Multiple variable (Declaring and initializing at the same time)
let var6 = 1,
  var7 = 2,
  var8 = 3;
