// Comparison Operators

// == Equal to
// === Equal value and equal type
// != Not equal
// !== Not equal value and not equal type
// > Greater than
// < Less than
// >= Greater than equal to
// <= Less than equal to

// NOTE
// - In JavaScript when you compare string and number it will treat string as number.

let var1 = "10";
let var2 = 10;
let var3 = var1 == var2;

console.log(var3);
